if (confirm('Почати тестування?')) {

/* 18                   
   Є n бактерій червоного кольору. Через 1 одиницю часу червона бактерія змінюється на зелену, потім ще через 1 одиницю часу ділиться на червону та зелену. Скільки буде всіх бактерій через k одиниць часу?
*/
// Оголошуємо глобальні змінні для збереження кількості бактерій

let red, green

// Функція отримує початкову кількість бактерій або встановлює значення за замовчуванням
function getInitialBacteria() {
   let n = prompt("Введіть початкову кількість бактерій (n > 0):", "1")
   return parseInt(n) || 1 // Якщо введено некоректне значення, встановлюємо 1
}

// Функція отримує кількість часу або встановлює значення за замовчуванням
function getTimeUnits() {
   let k = prompt("Введіть кількість одиниць часу (k ≥ 0):", "4")
   return parseInt(k) || 0 
}

// Функція обчислює кількість червоних і зелених бактерій через k одиниць часу
function countBacteria(n, k) {
   red = n // Початкові червоні бактерії
   green = 0

   for (let i = 1; i <= k; i++) {
      if (i % 2 === 1) { 
         // На непарний крок червоні стають зеленими
         green += red
         red = 0
      } else { 
         // На парний крок зелені діляться на червону + зелену
         red += green
      }
   }
}


   let n = getInitialBacteria()
   let k = getTimeUnits()
   
   countBacteria(n, k)

   document.write(`Початкова кількість червоних бактерій: ${n}<br>`)
   document.write(`Проміжок часу: ${k} <br> <br>`)
   document.write(`Червоні бактерії: ${red}<br>`)
   document.write(`Зелені бактерії: ${green}<br>`)
   document.write(`Загальна кількість бактерій: ${red + green}`)




}
